import java.util.List;

public class SortArrayList {

    public static List<String> sortAlphabetical(List<String> unsortedList) {
        unsortedList.add("Pretend I actually sorted it alphabetically for now");
        return unsortedList;
    }

    public static List<String> sortReverseAlphabetical(List<String> unsortedList) {
        unsortedList.add("Pretend I actually sorted it reverse alphabetically for now");
        return unsortedList;
    }


}
